import sys; args = sys.argv[1:]
idx = int(args[0])-60

myRegexLst = [
    r"/^(?!.*010)[10]*$/",
    r"/^(?!.*010|101)[10]*$/",
    r"/^(1|0)?[10]*\1$|^0$|^1$/",
    r"/\b((\w)(?!\w*\2\b))+\b/i",
    r"/^((\w)(?!\w*\2))*((\w)(?=(\w*\4){2,}))((\w)(?!\w*\6))*/i",
    r"/\b\w*(\w)(\w*\1){2,}\w*\b/i",
    r"/\b(?=\w*a\w*\b)(?=\w*e\w*\b)(?=\w*i\w*\b)(?=\w*o\w*\b)(?=\w*u\w*\b)\w*/i",
    r"/^(?!(1*01*01*)*$)(?=((0*10*10*)*|0*)$)[10]*$/",
    r"/^((1(01*0)*1)+0*)+$|^0$/",
    r"/^(?!((1(01*0)*1)+0*)*$)1[10]*$/"
]

if idx < len(myRegexLst):
  print(myRegexLst[idx])

